package chessgame.chess.piece;

/**
 * Type represents the type of chess Piece. Useful to obtain what type of piece is at a 
 * specific square on the board.
 * @author maxharsh
 *
 */
public enum Type {
	/** 
	 * Pawn chess piece
	 */
	PAWN,
	/**
	 * Knight chess piece
	 */
	KNIGHT,
	/**
	 * Bishop chess piece
	 */
	BISHOP,
	/**
	 * Rook chess piece
	 */
	ROOK,
	/**
	 * Queen chess piece
	 */
	QUEEN,
	/** 
	 * King chess piece 
	 */
	KING
}